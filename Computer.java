public class Computer {
    public String naam;
    public int ramGB;
    public boolean inGebruik;
    public int geheugenGB;
    public double prijs;

	//Hallo Jumbo

    public String getNaam() {
        return naam;
    }

    public void setNaam(String naam) {
        this.naam = naam;
    }

    public int getRamGB() {
        return ramGB;
    }

    public void setRamGB(int ramGB) {
        this.ramGB = ramGB;
    }

    public boolean isInGebruik() {
        return inGebruik;
    }

    public void setInGebruik(boolean inGebruik) {
        this.inGebruik = inGebruik;
    }

    public int getGeheugenGB() {
        return geheugenGB;
    }

    public void setGeheugenGB(int geheugenGB) {
        this.geheugenGB = geheugenGB;
    }

    public double getPrijs() {
        return prijs;
    }

    public void setPrijs(double prijs) {
        this.prijs = prijs;
    }
}
